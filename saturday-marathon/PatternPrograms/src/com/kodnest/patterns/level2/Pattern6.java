package com.kodnest.patterns.level2;

public class Pattern6{
	public static void main(String[]args) {
	//For loop to take care of number of lines
			for(int i =1;i<=5;i++) {
				
			//printing line no in each line
			for(int j=1;j<=5;j++) {
				System.out.print(i);
			}
			//printing cursor to next line
			System.out.println();
			}
		}
}

