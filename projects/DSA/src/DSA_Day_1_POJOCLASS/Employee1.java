package DSA_Day_1_POJOCLASS;

public class Employee1 
{
	private String name;
	private int age;
	private String gender;
	public Employee1() {
		//Compulsory
	}
	public void setName() {
		this.name = name;
	}
	public void setAge() {
		this.age=age;
	}
    public void setGender() {
    	this.gender=gender;
    }
    public String getName() {
    	return name;
    }
    public int getAge() {
    	return age;
    }
    public String getGender() {
    	return gender;
    }
}
