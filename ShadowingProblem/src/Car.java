
public class Car {
	String carname;
	String color;
	String brand;
	String owner;
	int carid;
	public Car(String carname, String color, String brand, String owner, int carid) {
		super();
		this.carname = carname;
		this.color = color;
		this.brand = brand;
		this.owner = owner;
		this.carid = carid;
	}
	

}
